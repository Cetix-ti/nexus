/**
 * Lookup + optional {param} interpolation.
 *   t("portal.home.greetingNamed", "en", { name: "Bruno" }) => "Hello, Bruno"
 */
export function t(
  key: string,
  locale: Locale = "fr",
  params?: Record<string, string | number>,
): string {
  const raw = translations[locale]?.[key] ?? translations.fr[key] ?? key;
  if (!params) return raw;
  return Object.keys(params).reduce(
    (s, k) => s.replace(new RegExp(`\\{${k}\\}`, "g"), String(params[k])),
    raw,
  );
}
